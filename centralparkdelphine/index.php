    var prefix = match[1];
    var numStr = match[2].replace(/,/g, '');
    var suffix = match[3];
    var target = parseFloat(numStr);
    if (isNaN(target) || target === 0) return;
    var isDecimal = numStr.indexOf('.') !== -1;
    var decimalPlaces = isDecimal ? (numStr.split('.')[1] || '').length : 0;
    var hasCommas = match[2].indexOf(',') !== -1;
    var duration = 1800;
    var startTime = null;

    function formatNum(n) {
        var s = isDecimal ? n.toFixed(decimalPlaces) : Math.round(n).toString();
        if (hasCommas) {
            var parts = s.split('.');
            parts[0] = parts[0].replace(/\B(?=(\d{3})+(?!\d))/g, ',');
            s = parts.join('.');
        }
        return s;
    }

    function step(timestamp) {
        if (!startTime) startTime = timestamp;
        var progress = Math.min((timestamp - startTime) / duration, 1);
        // Ease out cubic
        var eased = 1 - Math.pow(1 - progress, 3);
        var current = target * eased;
        el.textContent = prefix + formatNum(current) + suffix;
        if (progress < 1) requestAnimationFrame(step);
    }
    requestAnimationFrame(step);
}

var counterObserver = new IntersectionObserver(function(entries) {
    entries.forEach(function(entry) {
        if (entry.isIntersecting) {
            animateCounter(entry.target);
            counterObserver.unobserve(entry.target);
        }
    });
}, { threshold: 0.3 });

