        // Front-end validation
        function validateForm(form) {
            let name = form.querySelector('[name="name"]').value.trim();
            let email = form.querySelector('[name="email"]').value.trim();
            let phone = form.querySelector('[name="phone"]').value.trim();
            let emailPattern = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
            let phonePattern = /^[0-9]{10,15}$/;
            let errors = [];
            if (!name) errors.push('Name is required.');
            if (!email || !emailPattern.test(email)) errors.push('Valid email is required.');
            if (!phone || !phonePattern.test(phone)) errors.push('Valid phone number is required.');
            if (errors.length > 0) { alert(errors.join('\n')); return false; }
            return true;
        }
        function handleAjaxSubmit(form) {
            form.addEventListener('submit', function(e) {
                e.preventDefault();
                if (!validateForm(form)) return;

                var btn = form.querySelector('button[type="submit"]');
                var originalText = btn.innerHTML;
                btn.disabled = true;
                btn.innerHTML = 'Submitting...';

                fetch('process.php', { method: 'POST', body: new FormData(form) })
                    .then(function(r) { return r.json(); })
                    .then(function(data) {
                        if (data.success) {
                            form.reset();
                            window.location.href = data.redirect || 'thankyou.php';
                        } else {
                            alert(data.message || 'An error occurred.');
                        }
                    })
                    .catch(function() { alert('An unexpected error occurred. Please try again.'); })
                    .finally(function() { btn.disabled = false; btn.innerHTML = originalText; });
            });
        }
        if (document.getElementById('main-contact-form')) handleAjaxSubmit(document.getElementById('main-contact-form'));
        if (document.getElementById('modal-contact-form')) handleAjaxSubmit(document.getElementById('modal-contact-form'));
    </script>
