        function lazyLoadImages() {
            if (!('IntersectionObserver' in window)) {
                document.querySelectorAll('img[loading="lazy"]').forEach(img => {
                    img.src = img.dataset.src;
                    img.classList.remove('lazy');
                });
                return;
            }

            const obs = new IntersectionObserver((entries, o) => {
                entries.forEach(e => {
                    if (!e.isIntersecting) return;
                    const img = e.target;
                    img.src = img.dataset.src;
                    img.classList.remove('lazy');
                    o.unobserve(img);
                });
            });
            document.querySelectorAll('img[loading="lazy"]').forEach(img => obs.observe(img));
        }

        function initializeForms() {
            document.querySelectorAll('form').forEach(form => {
                // CSRF
                const tokenInput = document.createElement('input');
                tokenInput.type = 'hidden';
                tokenInput.name = 'csrf_token';
                tokenInput.value = generateCSRFToken();
                form.appendChild(tokenInput);

                // Input validation
                form.querySelectorAll('input').forEach(input => {
                    input.addEventListener('input', () => validateInput(input));
                    input.addEventListener('blur', () => validateInput(input));
                });

                // Submit handler
                form.addEventListener('submit', async e => {
                    e.preventDefault();
                    const inputs = form.querySelectorAll('input');
                    if (![...inputs].every(validateInput)) return;

                    const btn = form.querySelector('button[type="submit"]');
                    btn.classList.add('loading');
                    btn.disabled = true;

                    try {
                        const recaptchaToken = await generateRecaptchaToken();
                        if (!recaptchaToken) throw new Error();

                        const data = new FormData(form);
                        data.append('recaptcha_token', recaptchaToken);

                        const resp = await fetch(form.action || 'process.php', {
                            method: form.method || 'POST',
                            body: data
                        });
                        const result = await resp.json();

                        if (result.status === 'success') {
                            form.reset();
                            window.location = result.redirect || 'thank-you.php';
                        } else {
                            showNotification(result.message || 'An error occurred.', 'error');
                        }
                    } catch {
                        showNotification('Sorry, there was an error. Please try again.', 'error');
                    } finally {
                        btn.classList.remove('loading');
                        btn.disabled = false;
                    }
                });
            });
        }

