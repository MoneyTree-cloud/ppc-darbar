            function openGalleryModal(index) {
                const modal = new bootstrap.Modal(document.getElementById('galleryModal'));
                const carousel = document.getElementById('galleryCarousel');
                const items = carousel.querySelectorAll('.carousel-item');

                // Load images only when modal is opened
                items.forEach((item, i) => {
                    const img = item.querySelector('img');
                    if (!img.src || img.src === 'data:image/gif;base64,R0lGODlhAQABAIAAAAAAAP///yH5BAEAAAAALAAAAAABAAEAAAIBRAA7') {
                        img.src = img.dataset.src;
                        img.onload = function() {
                            img.classList.add('loaded');
                        };
                    }
                });

                items.forEach(item => item.classList.remove('active'));
                items[index].classList.add('active');
                modal.show();
            }

            // Initialize lazy loading
            document.addEventListener('DOMContentLoaded', function() {
                if ('IntersectionObserver' in window) {
                    const imageObserver = new IntersectionObserver((entries, observer) => {
                        entries.forEach(entry => {
                            if (entry.isIntersecting) {
                                const img = entry.target;
                                img.src = img.dataset.src;
                                img.onload = function() {
                                    img.classList.add('loaded');
                                };
                                observer.unobserve(img);
                            }
                        });
                    });

                    document.querySelectorAll('img.lazy').forEach(img => imageObserver.observe(img));
                } else {
                    // Fallback for browsers that don't support IntersectionObserver
                    document.querySelectorAll('img.lazy').forEach(img => {
                        img.src = img.dataset.src;
                        img.onload = function() {
                            img.classList.add('loaded');
                        };
                    });
                }
            });

            // Ensure modal cleanup on close
            document.getElementById('galleryModal').addEventListener('hidden.bs.modal', function() {
                const carousel = document.getElementById('galleryCarousel');
                const items = carousel.querySelectorAll('.carousel-item');
                items.forEach(item => item.classList.remove('active'));
                items[0].classList.add('active');
            });
