        function validateInput(input) {
            let ok = true;
            input.classList.remove('valid', 'invalid');
            const val = input.value.trim();

            if (input.type === 'text' && val.length < 2) ok = false;
            if (input.type === 'tel' && val.replace(/\D/g, '').length !== 10) ok = false;
            if (input.type === 'email' && !/^[^\s@]+@[^\s@]+\.[^\s@]+$/.test(val)) ok = false;

            if (val) input.classList.add(ok ? 'valid' : 'invalid');
            return ok;
