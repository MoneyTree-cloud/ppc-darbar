    <script>
        document.addEventListener('DOMContentLoaded', function() {
            const mobileMenuIcon = document.getElementById('mobileMenuIcon');
            const navLinks = document.getElementById('navLinks');

            // Mobile Menu Toggle
            mobileMenuIcon.addEventListener('click', () => {
                navLinks.classList.toggle('active');
                mobileMenuIcon.classList.toggle('active');
                document.body.classList.toggle('no-scroll');
            });

            // Close menu on off-click
            document.addEventListener('click', function(event) {
                const isClickInsideNav = navLinks.contains(event.target);
                const isClickOnIcon = mobileMenuIcon.contains(event.target);
                if (!isClickInsideNav && !isClickOnIcon && navLinks.classList.contains('active')) {
                    navLinks.classList.remove('active');
                    mobileMenuIcon.classList.remove('active');
                    document.body.classList.remove('no-scroll');
                }
            });

            // Popup Form Logic
            const popupTriggers = document.querySelectorAll('.popup-trigger');
            const popupForm = document.getElementById('enquiryForm');
            const closeFormBtn = popupForm.querySelector('.close-form');

            const openPopup = (e) => {
                e.preventDefault();
                popupForm.classList.add('active');
                document.body.classList.add('no-scroll');
            }
            const closePopup = () => {
                popupForm.classList.remove('active');
                if (!navLinks.classList.contains('active')) {
                    document.body.classList.remove('no-scroll');
                }
            }

            popupTriggers.forEach(trigger => trigger.addEventListener('click', openPopup));
            closeFormBtn.addEventListener('click', closePopup);
            popupForm.addEventListener('click', (e) => {
                if (e.target === popupForm) closePopup();
            });
            document.addEventListener('keydown', (e) => {
                if (e.key === 'Escape' && popupForm.classList.contains('active')) closePopup();
            });

            // Active Nav Link on Scroll
            const sections = document.querySelectorAll("section[id]");
            const headerNavLinks = document.querySelectorAll(".nav-links a");
            window.addEventListener("scroll", () => {
                let current = "";
                sections.forEach(section => {
                    const sectionTop = section.offsetTop;
                    if (pageYOffset >= sectionTop - 150) {
                        current = section.getAttribute("id");
                    }
                });

                headerNavLinks.forEach(link => {
                    link.classList.remove("active");
                    if (link.getAttribute("href").slice(1) === current) {
                        link.classList.add("active");
                    }
                });
            });

            // Intersection Observer for animations on scroll
            const scrollElements = document.querySelectorAll('.animate-on-scroll');
            const observer = new IntersectionObserver((entries) => {
                entries.forEach(entry => {
                    if (entry.isIntersecting) {
                        entry.target.classList.add('is-visible');
                        observer.unobserve(entry.target);
                    }
                });
            }, {
                threshold: 0.1
            });
            scrollElements.forEach(el => observer.observe(el));

            // Animated Number Counter
            const statsSection = document.getElementById('stats');
            let hasAnimated = false;
            const countUp = () => {
                const counters = statsSection.querySelectorAll('.stat-number');
                counters.forEach(counter => {
                    const target = +counter.getAttribute('data-target');
                    let current = 0;
                    const increment = target / 150;
                    const updateCounter = () => {
                        current += increment;
                        if (current < target) {
                            counter.innerText = Math.ceil(current);
                            requestAnimationFrame(updateCounter);
                        } else {
                            counter.innerText = target;
                        }
                    };
                    updateCounter();
                });
            };
            const statsObserver = new IntersectionObserver((entries) => {
                entries.forEach(entry => {
                    if (entry.isIntersecting && !hasAnimated) {
                        countUp();
                        hasAnimated = true;
                        statsObserver.unobserve(entry.target);
                    }
                });
            }, {
                threshold: 0.4
            });
            if (statsSection) statsObserver.observe(statsSection);

            // --- AJAX FORM SUBMISSION & TOAST NOTIFICATIONS ---

            /**
             * Shows a toast notification message.
             * @param {string} message The message to display.
             * @param {string} type The type of toast ('success' or 'error').
             * @param {number} duration The duration in ms to show the toast.
             */
            function showToast(message, type = 'success', duration = 5000) {
                const toast = document.createElement('div');
                toast.className = `toast ${type}`;
                toast.textContent = message;
                document.body.appendChild(toast);

                // Animate in
                setTimeout(() => {
                    toast.classList.add('show');
                }, 100);

                // Animate out and remove
                setTimeout(() => {
                    toast.classList.remove('show');
                    toast.addEventListener('transitionend', () => toast.remove());
                }, duration);
            }

            // Form Submission Handler
            document.querySelectorAll('.formSelectorClass').forEach(form => {
                form.addEventListener('submit', function(e) {
                    e.preventDefault();
                    console.log('Form submission started');

                    const submitButton = this.querySelector('button[type="submit"]');
                    const originalButtonText = submitButton.innerHTML;
                    submitButton.innerHTML = 'Sending...';
                    submitButton.disabled = true;

                    const formData = new FormData(this);
                    console.log('Form data:', Object.fromEntries(formData));

                    fetch('process.php', {
                            method: 'POST',
                            body: formData
                        })
                        .then(response => {
                            console.log('Response received:', response);
                            return response.json();
                        })
                        .then(data => {
                            console.log('Data processed:', data);
                            if (data.status === 'success') {
                                if (data.redirect) {
                                    console.log('Redirecting to:', data.redirect);
                                    window.location.href = data.redirect;
                                }
                            } else {
                                console.error('Error:', data.message);
                                alert(data.message);
                            }
                        })
                        .catch(error => {
                            console.error('Fetch error:', error);
                            alert('An error occurred. Please try again later.');
                        })
