    <script>
        document.addEventListener('DOMContentLoaded', function() {

            // --- Element Selectors ---
            const formContainer = document.getElementById('enquiry-form-container');
            const formOverlay = document.getElementById('form-overlay');
            const mobileMenuButton = document.getElementById('mobile-menu-button');
            const mobileMenu = document.getElementById('mobile-menu');
            const mobileNavLinks = document.querySelectorAll('.mobile-nav-link');
            const exitIntentOverlay = document.getElementById('exit-intent-overlay');
            const pageContactForm = document.getElementById('pageContactForm');
            const popupEnquiryForm = document.getElementById('popupEnquiryForm');
            const formWrapper = document.getElementById('form-wrapper');
            const successMessage = document.getElementById('success-message');

            // --- AJAX form handler ---
            const handleFormSubmit = (formElement) => {
                formElement.addEventListener('submit', function(event) {
                    event.preventDefault();

                    const formData = new FormData(formElement);
                    const submitButton = formElement.querySelector('button[type="submit"]');
                    const originalButtonText = submitButton.innerHTML;
                    submitButton.disabled = true;
                    submitButton.innerHTML = `<i class="fas fa-spinner fa-spin"></i> Submitting...`;

                    fetch('process.php', { method:'POST', body: formData, redirect:'follow' })
                      .then(response => {
                        if (response.redirected) { window.location.href = response.url; return; }
                        return response.json();
                      })
                      .then(data => {
                        if (data) {
                            if (data.status === 'success') {
                                showToast(data.message || 'Thanks! We will get back to you shortly.','success');
                                formElement.reset();
                                if (formElement.id === 'popupEnquiryForm') {
                                    formWrapper.classList.add('hidden');
                                    successMessage.classList.remove('hidden');
                                }
                            } else {
                                showToast(data.message || 'An error occurred','error');
                            }
                        }
                      })
                      .catch(() => showToast('An unexpected error occurred. Please try again.','error'))
                      .finally(() => {
                        submitButton.disabled = false;
                        submitButton.innerHTML = originalButtonText;
                      });
                });
            };

            handleFormSubmit(pageContactForm);
            handleFormSubmit(popupEnquiryForm);

            // --- Toast helper ---
            const showToast = (message, type='info') => {
                let className = 'toastify-info';
                if (type === 'success') className = 'toastify-success';
                if (type === 'error') className = 'toastify-error';
                Toastify({ text: message, duration: 3000, close: true, gravity: "top", position: "right", className }).showToast();
            };

            // --- Open/Close Enquiry Form ---
            window.openForm = function() {
                formOverlay.style.opacity = '1';
                formOverlay.classList.remove('hidden');
                formContainer.classList.add('open');
                document.body.style.overflow = 'hidden';
                formWrapper.classList.remove('hidden');
                successMessage.classList.add('hidden');
                popupEnquiryForm.reset();
            };

            window.closeForm = function() {
                formContainer.classList.remove('open');
                formOverlay.style.opacity = '0';
                setTimeout(() => formOverlay.classList.add('hidden'), 350);
                document.body.style.overflow = '';
            };

            // --- Exit Intent ---
            window.closeExitIntent = function() {
                exitIntentOverlay.classList.add('hidden');
                document.body.style.overflow = '';
                sessionStorage.setItem('exitIntentShown','true');
            };

            // --- Event listeners ---
            mobileMenuButton.addEventListener('click', () => mobileMenu.classList.toggle('hidden'));
            mobileNavLinks.forEach(link => link.addEventListener('click', () => mobileMenu.classList.add('hidden')));
            formOverlay.addEventListener('click', closeForm);
            document.addEventListener('keydown', e => { if (e.key === 'Escape') { closeForm(); closeExitIntent(); } });

            // Exit Intent trigger
            document.addEventListener('mouseleave', e => {
                if (!e.relatedTarget && !e.toElement && e.clientY < 10) {
                    if (sessionStorage.getItem('exitIntentShown')) return;
                    exitIntentOverlay.classList.remove('hidden');
                    document.body.style.overflow = 'hidden';
                }
            });
        });
    </script>
